void AProjectileBullet::OnHit(UPrimitiveComponent* HitComp, AActor* OtherActor, UPrimitiveComponent* OtherComp, FVector NormalImpulse, const FHitResult& Hit)
{
	// bullet -> weapon -> owner character
	auto* OwnerCharacter = Cast<ABlasternautCharacter>(GetOwner());
	if (OwnerCharacter)
	{
		auto* OwnerController = Cast<ABlasternautController>(OwnerCharacter->Controller);
		if (OwnerController)
		{
			// on server without ssr --> simply apply damage
			if (OwnerCharacter->HasAuthority() && !bUseServerSideRewind)
			{
				UGameplayStatics::ApplyDamage(OtherActor, Damage, OwnerController, this, UDamageType::StaticClass());
				Super::OnHit(HitComp, OtherActor, OtherComp, NormalImpulse, Hit); // destroys bullet
				return;
			}
		
			// using ssr and locally controlled
			auto* HitCharacter = Cast<ABlasternautCharacter>(OtherActor);
			if (bUseServerSideRewind && OwnerCharacter->GetLagCompensation() && OwnerCharacter->IsLocallyControlled() && HitCharacter)
			{
				OwnerCharacter->GetLagCompensation()->ServerProjectileScoreRequest(
					HitCharacter,
					TraceStart,
					InitialVelocity,
					OwnerController->GetServerTime() - OwnerController->SingleTripTime
				);
			}
		}
	}
	
}
